const orderRecordByColumns = (
  columns: string[],
  record: Record<string, unknown>,
) => Object.fromEntries(columns.map((column) => [column, record[column]]));

const getWorkspaceMemberName = (workspaceMemberId: string) => {
  const workspaceMember = WORKSPACE_MEMBER_DATA_SEEDS.find(
    (workspaceMember) => workspaceMember.id === workspaceMemberId,
  );

  return `${workspaceMember?.nameFirstName ?? ''} ${
    workspaceMember?.nameLastName ?? ''
  }`.trim();
};

const createBlocknoteBody = (text: string, id: string) =>
  JSON.stringify([
    {
      id,
      type: 'paragraph',
      props: {
        textColor: 'default',
        backgroundColor: 'default',
        textAlignment: 'left',
      },
      content: [{ type: 'text', text, styles: {} }],
      children: [],
    },
  ]);

const TANAMI_DEMO_BASE_DATE = new Date('2026-07-05T00:00:00.000Z');

const formatDateTimeFromBaseDate = (daysFromBaseDate: number, hour = 9) => {
  const date = new Date(TANAMI_DEMO_BASE_DATE);

  date.setUTCDate(date.getUTCDate() + daysFromBaseDate);
  date.setUTCHours(hour, 0, 0, 0);

  return date.toISOString();
};

const formatDateFromBaseDate = (daysFromBaseDate: number) =>
  formatDateTimeFromBaseDate(daysFromBaseDate).slice(0, 10);

const createAuditFields = (workspaceMemberId: string) => {
  const workspaceMemberName = getWorkspaceMemberName(workspaceMemberId);

  return {
    createdBySource: 'MANUAL',
    createdByWorkspaceMemberId: workspaceMemberId,
    createdByName: workspaceMemberName,
    updatedBySource: 'MANUAL',
    updatedByWorkspaceMemberId: workspaceMemberId,
    updatedByName: workspaceMemberName,
  };
};

